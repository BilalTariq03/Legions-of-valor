export function abilityText(name) {
  if (!name) return 'No ability.';
  const trimmed = String(name).trim();
  return ABILITY_TEXT[trimmed]
    || ABILITY_TEXT[trimmed.toLowerCase()]
    || 'This ability has no description yet.';
}
